package metodos;

public class Resposta {
	private double x, ea;
	private int i;

	public Resposta(double x, double ea, int i) {
		this.x = x;
		this.ea = ea;
		this.i = i;
	}

	public double getX() {
		return x;
	}

	public double getEa() {
		return ea;
	}
	
	public int getI() {
		return i;
	}
}
